static BAM_Status BAM_normalizedGradXyList_initFrame(void *kernelContext)
{
    BAM_NormalizedGradXyList_Context *context = (BAM_NormalizedGradXyList_Context *) kernelContext;
    BAM_Status status = 0;
    uint32_t i;
    uint32_t (*xSequence)[];
    uint32_t * pUpperLeftXy;

    context->pingPongFlag = 0;
    context->blockCnt     = 0;
    xSequence = (uint32_t(*)[])(void*)context->pXsequence;
    for ( i = 0 ; i < context->ctrlArgs.width; i++)
    {
      (*xSequence)[i] = (i << 16U);
    }

    pUpperLeftXy = (uint32_t *) context->pUpperLeftXyPtr[0];


    context->blockStartX  = context->ctrlArgs.frameStartX;
    context->blockStartY  = context->ctrlArgs.frameStartY;
    /* Initialize the upper left corner of input image */
    *pUpperLeftXy = ( (uint32_t)context->blockStartX << 16U ) + context->blockStartY;

    pUpperLeftXy = (uint32_t *) context->pUpperLeftXyPtr[1];
    *pUpperLeftXy = ( (uint32_t)context->blockStartX << 16U ) + context->blockStartY;

    EDMA_UTILS_memcpy2D(
        (void*)context->pReciSqrtLut,
        (const void*)gReciSqrtLut,
        (uint16_t)(RECI_SQRT_LUT_SIZE * RECI_SQRT_LUT_NUM_CPY),
        1u,
        0,
        0);

    *(context->pListSizePtr) = 0;

    context->pingPongFlag ^= 1U;

#if !(VCOP_HOST_EMULATION)

    vcop_gradients_xy_list_init(
          (uint8_t*)context->pInBlock[BAM_NORMALIZED_GRADXY_LIST_IN_DATA_PORT],
          (uint32_t*)context->pUpperLeftXyPtr[0],
          (uint8_t*)context->pInBlock[BAM_NORMALIZED_GRADXY_LIST_IN_EDGEMAP_PORT],
          (uint32_t *)(void*) context->pXsequence,
          (int32_t*)(void*)context->pGradXyPtr,
          (uint32_t *)context->pOutBlock[BAM_NORMALIZED_GRADXY_LIST_XY_PORT],
          (uint16_t *)context->pListSizePtr,
          context->ctrlArgs.width,
          context->ctrlArgs.height,
          context->ctrlArgs.pitchInData,
          context->ctrlArgs.pitchEdgeMap,
          (uint16_t*)(void*)context->pBlockGradientsXyList);

    vcop_gradients_xy_mag_lut_index_calulcation_init(
          (int16_t*)(void*)context->pGradXyPtr,
          (uint16_t *)(void*)context->pLutIdxPtr,
          (int8_t *)(void*)context->pLog4aPtr,
          0,/* WIll be updated in param block update */
          1u,
          0,
          (uint16_t*)(void*)context->pBlockGradientsXyMagLutIndexCalulcation);

    vcop_reciprocal_sqrt_lookup_init(
          (uint16_t *)(void*)context->pLutIdxPtr,
          (uint8_t *) context->pReciSqrtLut,
          (uint8_t *) context->pReciprocalLutOutput,
          0,/* WIll be updated in param block update */
          (uint16_t*)(void*)context->pBlockReciprocalSqrtLookup);

    vcop_gradients_xy_unit_vecs_init(
          (int16_t*)(void*)context->pGradXyPtr,
          (int8_t *)(void*)context->pLog4aPtr,
          (uint8_t *) context->pReciprocalLutOutput,
          (int16_t *)context->pOutBlock[BAM_NORMALIZED_GRADXY_LIST_UNIT_XY_PORT],
          0,/* WIll be updated in param block update */
          (uint16_t*)(void*)context->pBlockGradientsXyUnitVecs);

    /* This is the param block update for above three kernels to update the list size */
    vcop_gradients_xy_update_param_block_init(
          (uint16_t *)(void*)context->pBlockGradientsXyMagLutIndexCalulcation,
          (uint16_t *)(void*)context->pBlockReciprocalSqrtLookup,
          (uint16_t *)(void*)context->pBlockGradientsXyUnitVecs,
          (uint16_t *)context->pListSizePtr,
          (uint16_t *)context->pListSizeWbufPtr,
          (uint16_t *)(void*)context->pBlockListSizeUpdate);

#endif
    return status;
}

static BAM_Status BAM_normalizedGradXyList_Compute(void *kernelContext)
{
    BAM_NormalizedGradXyList_Context *context = (BAM_NormalizedGradXyList_Context *) kernelContext;

#if (VCOP_HOST_EMULATION)
    uint16_t width;
    uint16_t height;

    vcop_gradients_xy_list(
          (uint8_t*)context->pInBlock[BAM_NORMALIZED_GRADXY_LIST_IN_DATA_PORT],
          (uint32_t*)context->pUpperLeftXyPtr[0],
          (uint8_t*)context->pInBlock[BAM_NORMALIZED_GRADXY_LIST_IN_EDGEMAP_PORT],
          (uint32_t *) context->pXsequence,
          (int32_t*)context->pGradXyPtr,
          (uint32_t *)context->pOutBlock[BAM_NORMALIZED_GRADXY_LIST_XY_PORT],
          (uint16_t *)context->pListSizePtr,
          context->ctrlArgs.width,
          context->ctrlArgs.height,
          context->ctrlArgs.pitchInData,
          context->ctrlArgs.pitchEdgeMap);

    width  = *((uint16_t *)context->pListSizePtr);
    height = 1;
    /* Copy the list size to wbuf as expected by the next kernel */
    *context->pListSizeWbufPtr = width;

    vcop_gradients_xy_mag_lut_index_calulcation(
          (int16_t*)context->pGradXyPtr,
          (uint16_t *)context->pLutIdxPtr,
          (int8_t *)context->pLog4aPtr,
          width,
          height,
          0);

    vcop_reciprocal_sqrt_lookup(
          (uint16_t *)context->pLutIdxPtr,
          (uint8_t *) context->pReciSqrtLut,
          (uint8_t *) context->pReciprocalLutOutput,
          width);

    vcop_gradients_xy_unit_vecs(
          (int16_t*)context->pGradXyPtr,
          (int8_t *)context->pLog4aPtr,
          (uint8_t *) context->pReciprocalLutOutput,
          (int16_t *)context->pOutBlock[BAM_NORMALIZED_GRADXY_LIST_UNIT_XY_PORT],
          width);

#else
    vcop_gradients_xy_list_vloops((uint16_t*)(void*)context->pBlockGradientsXyList);
    /* This kernel will update the param block of the three kernels with the list size found by the previous kernel */
    vcop_gradients_xy_update_param_block_vloops((uint16_t*)(void*)context->pBlockListSizeUpdate);
    vcop_gradients_xy_mag_lut_index_calulcation_vloops((uint16_t*)(void*)context->pBlockGradientsXyMagLutIndexCalulcation);
    vcop_reciprocal_sqrt_lookup_vloops((uint16_t*)(void*)context->pBlockReciprocalSqrtLookup);
    vcop_gradients_xy_unit_vecs_vloops((uint16_t*)(void*)context->pBlockGradientsXyUnitVecs);

#endif

    context->blockCnt++;

    /* Update the upper left XY for each block */
    if ( context->blockCnt == context->ctrlArgs.numHorizontalBlk)
    {
      context->blockStartX = context->ctrlArgs.frameStartX;
      context->blockStartY += context->ctrlArgs.height;
      context->blockCnt = 0;
    }
    else
    {
      context->blockStartX += context->ctrlArgs.width;
    }

    /* Update the upper left corner */
    *(context->pUpperLeftXyPtr[context->pingPongFlag]) = ( (uint32_t)context->blockStartX << 16U ) + context->blockStartY;

    context->pingPongFlag ^= 1U;

    return 0;
}

