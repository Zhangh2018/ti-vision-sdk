static void CreatePattern(uint8_t * p, uint16_t imgWidth, uint16_t imgHeight, uint16_t imgPitch, uint8_t maxVal)
{
  int32_t i, j;

  for ( j = 0 ; j < imgWidth; j++)
  {
    for (i = 0; i < imgHeight; i++)
    {
#ifdef FIXEDPATTERN
      p[i + j * imgPitch] = (i * j)% maxVal;

#else
      p[i + j * imgPitch] = rand() % maxVal;
#endif
    }
  }


}


static void CreatePatternWithEdgeMap(uint8_t * p, uint8_t * edgeMap,uint16_t imgWidth, uint16_t imgHeight, uint16_t imgPitch)
  {
    int32_t i, j;

    for ( j = 0 ; j < imgWidth; j++)
    {
      for (i = 0; i < imgHeight; i++)
      {
#ifdef FIXEDPATTERN
        p[i + j * imgPitch] = (i * j)% 255;
        edgeMap[(i + j * imgPitch - 2)] = 1;

#else
        p[i + j * imgPitch] = rand() % 255;
        edgeMap[(i + j * imgPitch -2 )] = rand() % 2;
#endif
      }
    }


  }

