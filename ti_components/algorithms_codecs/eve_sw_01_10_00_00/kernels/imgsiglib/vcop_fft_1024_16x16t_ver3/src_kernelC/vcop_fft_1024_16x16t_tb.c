/*  Top level FFT function, which leverages repeat loops for first 3 stages */
/*  It uses a regular loop for 4th and 5 th stage, with pointer increments  */
/*  passed as arguments. It then does a seperate digit reversal loop.       */
/*  This is a good example, where RISC core queues up back to back loops    */
/*  for execution on the vector core.                                       */
/*--------------------------------------------------------------------------*/ 

void vcop_fft_1024_16x16t_cn (short in[2304], short SC[2216], unsigned short digit_rev[256], short out[2304])
{
    vcop_fft_1024_16x16t_stage_1_to_2
    (
        pxi0, 
        pxi1, 
        pxi2, 
        pxi3, 
        cs1, 
        cs2, 
        cs3, 
        bfys_per_grp,
        grps,
        passes_rpt,
        pinc3,
        pinc4,
        ptnc3,
        ptnc4
     );

     vcop_fft_1024_16x16t_stage3
     (
        &in[0],
        &in[2*16],
        &in[2*32],
        &in[2*48],
        &SC[2 * 3 * (256 + 64)],
        &SC[2 * 3 * (256 + 64) + (2 *  8)],
        &SC[2 * 3 * (256 + 64) + (2 * 16)],
        &out[2 * 0],
        &out[2 * 16 * 9],
        &out[2 * 32 * 9],
        &out[2 * 48 * 9],
        &out[2 * 0        + 1],
        &out[2 * 16 * 9   + 1],
        &out[2 * 32 * 9   + 1],
        &out[2 * 48 * 9   + 1]
     );


     vcop_fft_1024_16x16t_stage4
     (
         &out[0],
         &out[2 *  4 * 9],
         &out[2 *  8 * 9],
         &out[2 * 12 * 9],
         &SC[ 2 * 3 * (256 + 64 + 16)],
         &SC[ 2 * 3 * (256 + 64 + 16) + (2 * 8)],
         &SC[ 2 * 3 * (256 + 64 + 16) + (2 * 16)],
         &in[0],
         &in[2 *  4 * 9],
         &in[2 *  8 * 9],
         &in[2 * 12 * 9]
    );

    vcop_fft_1024_16x16t_stage5
    (
         &in[0],
         &in[2 * 9],
         &in[2 * 2 * 9],
         &in[2 * 3 * 9],
         &out[0],
         &out[2 * 256],
         &out[2 * 512],
         &out[2 * 768] 
    );

}

