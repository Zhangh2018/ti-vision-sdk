/*         unsigned short   out_h,                                          */
/*         unsigned short  *outptr                                          */
/*     );                                                                   */
/*                                                                          */
/*                                                                          */
/*      *inrgb555_ptr   :  Pointer to an rgb555 source array.               */
/*      *back_rgb555_ptr:  Pointer to an rgb555 background array.           */
