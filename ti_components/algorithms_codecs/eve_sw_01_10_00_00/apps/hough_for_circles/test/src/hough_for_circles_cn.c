void genEdgeMap_cn(
  uint8_t  *inputData,
  uint8_t  *edgeMap,
  uint16_t imgWidth,
  uint16_t imgHeight,
  uint16_t imgPitch,
  uint16_t edgeMapPitch,
  uint16_t gradThr

  )
{
  int16_t i,j;
  int16_t * gradX = NULL;
  int16_t * gradY = NULL;
  int16_t * gradMag = NULL;
  uint8_t * edgePtr1x1;

  edgePtr1x1 = edgeMap + edgeMapPitch + 1;

  gradX   = (int16_t *)malloc( imgWidth * imgHeight * sizeof (int16_t) );
  gradY   = (int16_t *)malloc(imgWidth * imgHeight * sizeof (int16_t));
  gradMag = (int16_t *)malloc(imgWidth * imgHeight * sizeof (int16_t));

  vcop_vec_gradients_xy_and_magnitude_cn(inputData,
                                             gradX,
                                             gradY,
                                             gradMag,
                                             imgWidth,
                                             imgHeight,
                                             imgPitch
                                             );

  for ( j = 0 ; j < imgHeight - 2; j++)
  {
    for ( i = 0 ; i < imgWidth - 2; i++ )
    {
      if ( gradMag[ i + ( j * imgWidth )] > gradThr )
      {
        edgePtr1x1[i + ( j * edgeMapPitch)] = 1;
      }
      else
      {
        edgePtr1x1[i + ( j * edgeMapPitch)] = 0;
      }
    }
  }

  if ( gradX )
  {
    free ( gradX );
  }

  if ( gradY )
  {
    free ( gradY );
  }

  if ( gradMag)
  {
    free ( gradMag );
  }
}


