/*  USAGE                                                                   */
/*      This code is a driver file for testing the 'eve_dct8x8col_char'     */
/*  function by injecting it with the same data set as in the               */
/*  assembly testbench functions.                                           */
/*                                                                          */
/*  This testbench then compares the results with the expected output       */
/*  array "EOut" by doing a standard "memcmp" and printing a pass/fail      */
/*  message. If it failed it prints a byte by byte trace.                   */
/*                                                                          */
/*     ASSUMPTIONS                                                          */
/*     - While The C code does not have any assumptions on the size of      */
/*     the arrays, the assembly code assumes that the DCT is computed on    */
/*     8x8 block of input at a time.                                        */
/*     - The DCT coefficients are implicitly defined. There is no need to   */
/*     pass them from the calling function to the callee.                   */
/*     - Input data is in Q12 format, DCT coefficients are in Q15 format.   */
/*                                                                          */
/*  To build the "C" executable type:                                       */
/*                                                                          */
/*  gcc *.c -lm -o a.out                                                    */
