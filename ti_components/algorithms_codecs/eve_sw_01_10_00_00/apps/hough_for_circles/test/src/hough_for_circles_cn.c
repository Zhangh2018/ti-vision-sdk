#define HOUGH_INDEX_CALCULATION_MAX_HOUGHSPACE_WIDTH  (128)
#define HOUGH_INDEX_CALCULATION_MAX_HOUGHSPACE_HEIGHT (128)

#define HOUGH_FOR_CIRCLES_CEIL(x,y) ((( x + y - 1) / y) * y)
#define HOUGH_FOR_CIRCLES_FLOOR(x,y) (( x  / y) * y)

#define HOUGH_FOR_CIRCLES_MIN(a, b) ((a) < (b) ? (a) : (b))

#define HOUGH_INDEX_CALCULATION_MAX_BLOCK_WIDTH   (48U)
#define HOUGH_INDEX_CALCULATION_MAX_BLOCK_HEIGHT  (48U)

#define HOUGH_INDEX_CALCULATION_BORDER_X (2U)
#define HOUGH_INDEX_CALCULATION_BORDER_Y (2U)


static int32_t houghForCircle_getTileDimension(
                  uint16_t  imageWidth,
                  uint16_t  imageHeight,
                  HOUGH_FOR_CIRCLES_TI_CreateParams *createparams,
                  HOUGH_FOR_CIRCLES_TI_InArgs *inArgs,
                  HOUGH_FOR_CIRCLES_TI_ControlOutArgs * ctrlOutArgs,
                  uint16_t * tileWidth,
                  uint16_t * tileHeight,
                  uint16_t * outTileWidth,
                  uint16_t * outTileHeight,
                  uint16_t * lastRowTileWidth,
                  uint16_t * lastColTileHeight,
                  uint16_t * numHorzTile,
                  uint16_t * numVertTile,
                  uint8_t  * maxRadius)
{
  int32_t status = IALG_EOK;

  uint16_t maxTileWidth;
  uint16_t maxTileHeight;
  uint16_t houghSpaceScalingShift;
  uint16_t computeBlockWidth;
  uint16_t computeBlockHeight;
  uint16_t radiusIdx;
  uint8_t radiusMax;
  uint16_t effImageWidth;
  uint16_t effImageHeight;
  uint16_t ceilImageWidth;
  uint16_t ceilImageHeight;

  computeBlockWidth   = HOUGH_INDEX_CALCULATION_MAX_BLOCK_WIDTH;
  computeBlockHeight  = HOUGH_INDEX_CALCULATION_MAX_BLOCK_HEIGHT;

  houghSpaceScalingShift = createparams->houghSpaceScaling;

  radiusMax = 0;

  for ( radiusIdx = 0; radiusIdx < inArgs->numRadius; radiusIdx++ )
  {
    if (inArgs->circleProps[radiusIdx].radius > radiusMax)
    {
      radiusMax = inArgs->circleProps[radiusIdx].radius;
    }
  }

  maxTileWidth   = ( HOUGH_INDEX_CALCULATION_MAX_HOUGHSPACE_WIDTH << houghSpaceScalingShift );

  maxTileHeight  = ( HOUGH_INDEX_CALCULATION_MAX_HOUGHSPACE_HEIGHT << houghSpaceScalingShift );

  maxTileWidth = HOUGH_FOR_CIRCLES_FLOOR( (maxTileWidth - HOUGH_INDEX_CALCULATION_BORDER_X) , computeBlockWidth ) +
                                  HOUGH_INDEX_CALCULATION_BORDER_X;

  maxTileHeight = HOUGH_FOR_CIRCLES_FLOOR( (maxTileHeight - HOUGH_INDEX_CALCULATION_BORDER_Y) , computeBlockHeight) +
                                  HOUGH_INDEX_CALCULATION_BORDER_Y;

  ceilImageWidth = HOUGH_FOR_CIRCLES_CEIL( (imageWidth - HOUGH_INDEX_CALCULATION_BORDER_X) , computeBlockWidth ) +
                                  HOUGH_INDEX_CALCULATION_BORDER_X;

  ceilImageHeight = HOUGH_FOR_CIRCLES_CEIL( (imageHeight - HOUGH_INDEX_CALCULATION_BORDER_Y) , computeBlockHeight) +
                                  HOUGH_INDEX_CALCULATION_BORDER_Y;


  *tileWidth    = HOUGH_FOR_CIRCLES_MIN(ceilImageWidth , maxTileWidth );
  *outTileWidth = *tileWidth - 2 * radiusMax;

  *tileHeight     = HOUGH_FOR_CIRCLES_MIN(ceilImageHeight, maxTileHeight );
  *outTileHeight = *tileHeight - 2 * radiusMax;

  *lastRowTileWidth   = *tileWidth;
  *lastColTileHeight  = *tileHeight;

  effImageWidth   = *tileWidth;
  effImageHeight  = *tileHeight;

  if (  ceilImageWidth > *tileWidth )
  {
    effImageWidth  = HOUGH_FOR_CIRCLES_FLOOR( ( imageWidth  - 2 * radiusMax) , *outTileWidth  ) + 2 * radiusMax;
    if ( effImageWidth != imageWidth)
    {
      *lastRowTileWidth = HOUGH_FOR_CIRCLES_CEIL( ( ( imageWidth - effImageWidth + 2 * radiusMax ) - HOUGH_INDEX_CALCULATION_BORDER_X),
                                                        computeBlockWidth)  + HOUGH_INDEX_CALCULATION_BORDER_X;

      effImageWidth = ( effImageWidth  - 2 * radiusMax) + *lastRowTileWidth;
    }
  }

  if (  ceilImageHeight >  *tileHeight )
  {
    effImageHeight  = HOUGH_FOR_CIRCLES_FLOOR( ( imageHeight  - 2 * radiusMax) , *outTileHeight  ) + 2 * radiusMax;
    if ( effImageHeight != imageHeight )
    {
      *lastColTileHeight = HOUGH_FOR_CIRCLES_CEIL( ( ( imageHeight - effImageHeight + 2 * radiusMax ) - HOUGH_INDEX_CALCULATION_BORDER_Y),
                                                        computeBlockHeight)  + HOUGH_INDEX_CALCULATION_BORDER_Y;

      effImageHeight = ( effImageHeight  - 2 * radiusMax) + *lastColTileHeight;
    }
  }


  *numHorzTile = HOUGH_FOR_CIRCLES_CEIL(( effImageWidth - 2 * radiusMax) ,  *outTileWidth ) / *outTileWidth;
  *numVertTile = HOUGH_FOR_CIRCLES_CEIL(( effImageHeight - 2 * radiusMax) ,  *outTileHeight ) / *outTileHeight;


  ctrlOutArgs->effImageWidth  = effImageWidth;
  ctrlOutArgs->effImageHeight = effImageHeight;

  *maxRadius        = radiusMax;

  return (status);
}

