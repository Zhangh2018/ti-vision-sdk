void vcop_grayscale_morphology_cn(
            grayscaleMorphologyStructuringElement  structuringElementType,
            grayscaleMorphologyMode                morphologyMode,
            uint16_t  blkWidth,      // width of input block, in elements
            uint16_t  blkStride,     // offset between input lines, in elements
            uint16_t  blkHeight,     // height of input block
            uint8_t  *dataPtr_A,     // input data pointer
            uint16_t  seWidth,       // width of structuring element block, in elements
            uint16_t  seHeight,      // height of structuring element block
            uint8_t  *sePtr_C,       // structuring element data pointer
            uint8_t  *seReflPtr_C,
            uint8_t  *scratchPtr_C,
            uint8_t  *scratchPtr_A,
            uint8_t  *outputPtr_B)   // output data pointer
{

  switch ( morphologyMode )
  {
    case grayscaleMorphologyDilation:
      vcop_grayscale_dilate_mask_cn(blkWidth, blkStride, blkHeight, dataPtr_A,
          seWidth, seHeight, seReflPtr_C, outputPtr_B);

    break;
    case grayscaleMorphologyErosion:
      vcop_grayscale_erode_mask_cn(blkWidth, blkStride, blkHeight, dataPtr_A,
          seWidth, seHeight, sePtr_C, outputPtr_B);

    break;
    case grayscaleMorphologyOpening:
      vcop_grayscale_open_mask_cn(blkWidth, blkStride, blkHeight, dataPtr_A,
          seWidth, seHeight, sePtr_C, seReflPtr_C, scratchPtr_C, outputPtr_B);

    break;
    case grayscaleMorphologyClosing:
      vcop_grayscale_close_mask_cn(blkWidth, blkStride, blkHeight, dataPtr_A,
          seWidth, seHeight, sePtr_C, seReflPtr_C, scratchPtr_C, outputPtr_B);

    break;
    case grayscaleMorphologyTopHat:
      vcop_grayscale_tophat_mask_cn(blkWidth, blkStride, blkHeight, dataPtr_A,
          seWidth, seHeight, sePtr_C, seReflPtr_C, scratchPtr_C, outputPtr_B);

    break;
    case grayscaleMorphologyBottomHat:
      vcop_grayscale_bottomhat_mask_cn(blkWidth, blkStride, blkHeight, dataPtr_A,
          seWidth, seHeight, sePtr_C, seReflPtr_C, scratchPtr_C, outputPtr_B);

    break;
    case grayscaleMorphologyMorphologicalGradient:
      vcop_grayscale_morph_grad_mask_cn(blkWidth, blkStride, blkHeight, dataPtr_A,
          seWidth, seHeight, sePtr_C, seReflPtr_C, scratchPtr_C, scratchPtr_A, outputPtr_B);

    break;
  }

}



void test_grayscale_morphology(sGrayscale_Morphology_Config *testcaseParams)
{
    uint8_t *dataPtr_A, *sePtr_C, *seReflPtr_C, *scratchPtr_C, *scratchPtr_A, *outputPtr_B, *refOutputPtr;
    uint8_t  fail, i, j, sizeReductionFactor;
    FILE *fp;

    profiler_init();

#if VCOP_HOST_EMULATION
    /*-----------------------------------------------------------------
          Open Input File. If non existant, fill input with data.
          -----------------------------------------------------------------*/
    fp = fopen((char *)testcaseParams->inImage, "rb");
    if(fp == NULL)
    {
        /*-----------------------------------------------------------------
              Fill input with data
              -----------------------------------------------------------------*/
        dataPtr_A = (uint8_t *)Morphology_grayscale_IN;
    } else
    {
        dataPtr_A = (uint8_t *)malloc(testcaseParams->blkStride*testcaseParams->blkHeight);
        fread(dataPtr_A, 1, testcaseParams->blkStride*testcaseParams->blkHeight, fp);
        fclose(fp);
    }

    sePtr_C      = (uint8_t *)malloc(testcaseParams->seWidth*testcaseParams->seHeight);
    seReflPtr_C  = (uint8_t *)malloc(testcaseParams->seWidth*testcaseParams->seHeight);
    scratchPtr_C = (uint8_t *)malloc(testcaseParams->blkStride*testcaseParams->blkHeight);
    scratchPtr_A = (uint8_t *)malloc(testcaseParams->blkStride*testcaseParams->blkHeight);
    outputPtr_B  = (uint8_t *)malloc(testcaseParams->blkStride*testcaseParams->blkHeight);

#else
    vcop_minit();
    /*-----------------------------------------------------------------
          Open Input File. If non existant, fill input with data.
          -----------------------------------------------------------------*/
    fp = fopen((char *)testcaseParams->inImage, "rb");
    if(fp == NULL)
    {
        /*-----------------------------------------------------------------
              Fill input with data
              -----------------------------------------------------------------*/
        dataPtr_A = (uint8_t *)Morphology_grayscale_IN;
    } else
    {
        dataPtr_A = (uint8_t *)vcop_malloc(VCOP_IBUFLA, testcaseParams->blkStride*testcaseParams->blkHeight);
        fread(dataPtr_A, 1, testcaseParams->blkStride*testcaseParams->blkHeight, fp);
        fclose(fp);
    }

    sePtr_C      = (uint8_t *)vcop_malloc(VCOP_WMEM,   testcaseParams->seWidth*testcaseParams->seHeight);
    seReflPtr_C  = (uint8_t *)vcop_malloc(VCOP_WMEM,   testcaseParams->seWidth*testcaseParams->seHeight);
    scratchPtr_C = (uint8_t *)vcop_malloc(VCOP_WMEM,   testcaseParams->blkStride*testcaseParams->blkHeight);
    scratchPtr_A = (uint8_t *)vcop_malloc(VCOP_IBUFLA, testcaseParams->blkStride*testcaseParams->blkHeight);
    outputPtr_B  = (uint8_t *)vcop_malloc(VCOP_IBUFHA, testcaseParams->blkStride*testcaseParams->blkHeight);
#endif
    refOutputPtr  = (uint8_t *)malloc(testcaseParams->blkStride*testcaseParams->blkHeight);

    switch ( testcaseParams->structuringElementType )
    {
      case grayscaleMorphologyMask:
        memset(sePtr_C, 0, testcaseParams->seWidth*testcaseParams->seHeight);

        for(i = 0; i < testcaseParams->seHeight * testcaseParams->seWidth; i++)
        {
            sePtr_C[i] = testcaseParams->seCoefficient[i];
        }

      break;
      case grayscaleMorphologyRect:
        memset(sePtr_C, 1, testcaseParams->seWidth*testcaseParams->seHeight);

      break;
      case grayscaleMorphologyCross:
        memset(sePtr_C, 0, testcaseParams->seWidth*testcaseParams->seHeight);

        for(i = 0; i < testcaseParams->seHeight; i++)
        {
          sePtr_C[i*testcaseParams->seWidth + (testcaseParams->seWidth-1)/2] = 1;
        }
        for(i = 0; i < testcaseParams->seWidth; i++)
        {
          sePtr_C[((testcaseParams->seHeight-1)/2)*testcaseParams->seWidth + i] = 1;
        }

      break;
    }

    for(i = 0; i < testcaseParams->seHeight; i++)
    {
      for(j = 0; j < testcaseParams->seWidth; j++)
      {
        seReflPtr_C[j*testcaseParams->seWidth + i] = sePtr_C[i*testcaseParams->seWidth + j];
      }
    }


#if !(VCOP_HOST_EMULATION)
    VCOP_BUF_SWITCH_SET(WBUF_SYST, IBUFHB_SYST, IBUFLB_SYST, IBUFHA_SYST, IBUFLA_SYST);

    // Note: Pass reflected structuring element to dilation kernel
    vcop_grayscale_morphology_init(
                testcaseParams->structuringElementType,
                testcaseParams->morphologyMode,
                testcaseParams->blkWidth,
                testcaseParams->blkStride,
                testcaseParams->blkHeight,
                dataPtr_A,
                testcaseParams->seWidth,
                testcaseParams->seHeight,
                sePtr_C,
                seReflPtr_C,
                scratchPtr_C,
                scratchPtr_A,
                outputPtr_B);

    VCOP_BUF_SWITCH_SET(WBUF_VCOP, IBUFHB_VCOP, IBUFLB_VCOP, IBUFHA_VCOP, IBUFLA_VCOP);
#endif

    for (i = 0; i < 2; i++)
    {
        memset(outputPtr_B, 0, testcaseParams->blkStride*testcaseParams->blkHeight);

        profiler_start();
        /*---------------------------------------------------------------------*/
        /*  Function call to the VCOP-C API to be tested                       */
        /*---------------------------------------------------------------------*/
#if VCOP_HOST_EMULATION
        vcop_grayscale_morphology_exec(
                    testcaseParams->structuringElementType,
                    testcaseParams->morphologyMode,
                    testcaseParams->blkWidth,
                    testcaseParams->blkStride,
                    testcaseParams->blkHeight,
                    dataPtr_A,
                    testcaseParams->seWidth,
                    testcaseParams->seHeight,
                    sePtr_C,
                    seReflPtr_C,
                    scratchPtr_C,
                    scratchPtr_A,
                    outputPtr_B);
#else
        vcop_grayscale_morphology_vloops(testcaseParams->structuringElementType,
                                         testcaseParams->morphologyMode);
        _vcop_vloop_done();
#endif

        profiler_end_print(1);
        profiler_performanceParam(testcaseParams->blkWidth * testcaseParams->blkHeight);
    }


#if !(VCOP_HOST_EMULATION)
    VCOP_BUF_SWITCH_SET(WBUF_SYST, IBUFHB_SYST, IBUFLB_SYST, IBUFHA_SYST, IBUFLA_SYST);
#endif

    memset(refOutputPtr, 0, testcaseParams->blkStride*testcaseParams->blkHeight);

    /*---------------------------------------------------------------------*/
    /*  Call the natural C implementation for grayscale morphology funcs.  */
    /*---------------------------------------------------------------------*/
    vcop_grayscale_morphology_cn(
                testcaseParams->structuringElementType,
                testcaseParams->morphologyMode,
                testcaseParams->blkWidth,
                testcaseParams->blkStride,
                testcaseParams->blkHeight,
                dataPtr_A,
                testcaseParams->seWidth,
                testcaseParams->seHeight,
                sePtr_C,
                seReflPtr_C,
                scratchPtr_C,
                scratchPtr_A,
                refOutputPtr);

    /*---------------------------------------------------------------------*/
    /*  Compare the results of output arrays with expected output arrays.  */
    /*  If there is a mis-compare, print out a memory trace, else print    */
    /*  pass.                                                              */
    /*---------------------------------------------------------------------*/
    fail = 0;

    if(testcaseParams->morphologyMode >= grayscaleMorphologyOpening)
    {
        sizeReductionFactor = 2;
    } else
    {
        sizeReductionFactor = 1;
    }

    for (i = 0; i < (testcaseParams->blkHeight-sizeReductionFactor*(testcaseParams->seHeight-1)); i++)
    {
        for (j = 0; j < (testcaseParams->blkWidth-sizeReductionFactor*(testcaseParams->seWidth-1)); j++)
        {
            if (outputPtr_B[i*testcaseParams->blkStride+j] !=
                refOutputPtr[i*testcaseParams->blkStride+j])
            {
                fail = 1;
                printf ("i:%d, j:%d, Out:%u, Ref_Out:%u \n", i, j, outputPtr_B[i*testcaseParams->blkStride+j],
                                                      refOutputPtr[i*testcaseParams->blkStride+j]);
            }
        }
    }

    profiler_printStatus(fail);
    profiler_deinit();

    fp = fopen((char *)testcaseParams->outImage, "wb");
    if(fp != NULL)
    {
        fwrite(outputPtr_B, 1, testcaseParams->blkStride*testcaseParams->blkHeight, fp);
        fclose(fp);
    }

#if VCOP_HOST_EMULATION
    if(dataPtr_A != Morphology_grayscale_IN)
    {
        free(dataPtr_A);
    }
    free(sePtr_C);
    free(seReflPtr_C);
    free(scratchPtr_C);
    free(scratchPtr_A);
    free(outputPtr_B);
#else

    if(dataPtr_A != Morphology_grayscale_IN)
    {
        vcop_free(dataPtr_A);
    }
    vcop_free(sePtr_C);
    vcop_free(seReflPtr_C);
    vcop_free(scratchPtr_C);
    vcop_free(scratchPtr_A);
    vcop_free(outputPtr_B);
#endif

    free(refOutputPtr);

}


int32_t main(int32_t argc, char *argv[])
{
  int status = 0;
  char configFile[FILE_NAME_SIZE]={0};
  char configLine[MAX_CONFIG_LINE_SIZE];
  char configParam[MAX_PARAM_STRING_SIZE];
  int8_t *LinePtr;
  FILE *fp;
  int32_t lineNum = -1, config_cmd;

  if (argc > 1) {
    strcpy(configFile, argv[1]) ;
  }
  else {
    strcpy(configFile, "../testvecs/config/config_list.txt");
  }

  fp = fopen((const char *)configFile , "r");
  if(fp== NULL) {
    printf("Could not open config list file : %s \n", configFile);
    return(status);
  }

  CF_profile_init(3, "vcop_grayscale_morphology");

  while(1) {
    memset(configLine, 0, MAX_CONFIG_LINE_SIZE);
    status = (int)fgets(configLine,MAX_CONFIG_LINE_SIZE,fp);
    LinePtr = (int8_t *)configLine;
    if ((status == EOF) || (status == 0)) {
      break;
    }

    status = 0;
    lineNum++;
    sscanf((const char *)LinePtr, "%d",&config_cmd);
    sscanf((const char *)LinePtr, "%s",configParam);

    if(config_cmd == 0) {
      printf("End of config list found !\n");
      break;
    }
    else if(config_cmd == 2) {
      continue;
    }
    else if(config_cmd == 1) {
      LinePtr = (int8_t *)moveToNextElement(configParam,(char *)LinePtr);
      status  = sscanf((const char *)LinePtr, "%s",configFile);
      printf("\nProcessing config file %s !\n", configFile);
      status = readparamfile(configFile, &gsTokenMap_grayscale_morphology[0]);
      if(status == -1) {
        printf("Parser Failed");
        return -1 ;
      }

      /* Validate the kernel */
      profiler_testCaseInfo((int8_t *)gConfig_grayscale_morphology.testCaseName, (int8_t *)" ", 0);
      printparams(&gsTokenMap_grayscale_morphology[0], (int32_t)MAX_ITEMS_TO_PARSE,
                  (uint32_t)(&gConfig_grayscale_morphology),sizeof(sGrayscale_Morphology_Config));

      test_grayscale_morphology(&gConfig_grayscale_morphology);

      CF_formula_add_test((gConfig_grayscale_morphology.blkWidth *
                           gConfig_grayscale_morphology.blkHeight),
                          (gConfig_grayscale_morphology.seWidth *
                           gConfig_grayscale_morphology.seHeight),
                           NULL, NULL, 0, NULL , 1);

    }
    else {
      printf("Unsupported config list command parameter at line num : %4d !\n", lineNum);
    }
  }

  CF_profile_setMode(3, 1);
  CF_profile_cycle_report(CF_PROFILE_FORMULA_RANGE, "N = num pixels M = SE size");

  fclose(fp);

  return status;
}

